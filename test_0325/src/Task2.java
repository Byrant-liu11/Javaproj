public class Task2 {
    public static void main(String[] args) {
        int result1 = integerAdd(2,3);
        System.out.println(result1);
        double result2 = decimalRepresentationAdd(1.2,5.5);
        System.out.println(result2);
    }
    public static int integerAdd(int a,int b) {
        return a + b;
    }
    public static double decimalRepresentationAdd(double a,double b) {
        return a + b;
    }
}
